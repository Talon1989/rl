import numpy as np


def iterative_geometric_series(r: float, start: int, end: int):
    assert 0 < r < 1
    counter = 0
    for i in range(start, end):
        counter += r**i
    return counter


def formula_geometric_series(r: float, start: int, end: int):
    assert 0 < r < 1
    return ((r ** start) * (1 - r ** end)) / (1 - r)








































































































































































































































